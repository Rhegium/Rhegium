@SuppressWarnings("serial")
public class ProtocolAttributeAccessorException extends RuntimeException {

	public ProtocolAttributeAccessorException() {
		super();
	}

	public ProtocolAttributeAccessorException(String message, Throwable cause) {
		super(message, cause);
	}

	public ProtocolAttributeAccessorException(String message) {
		super(message);
	}

	public ProtocolAttributeAccessorException(Throwable cause) {
		super(cause);
	}

}
